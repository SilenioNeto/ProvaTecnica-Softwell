package prova.model;

public class Usuario {
	private int pes_id; 
	private String pes_nome; 
	private String pes_sexo;
	private String pes_email;
	private String pes_celular; 
	private String pes_senha;
	private String pes_data_cadastro;
	public int getPes_id() {
		return pes_id;
	}
	public void setPes_id(int pes_id) {
		this.pes_id = pes_id;
	}
	public String getPes_nome() {
		return pes_nome;
	}
	public void setPes_nome(String pes_nome) {
		this.pes_nome = pes_nome;
	}
	public String getPes_sexo() {
		return pes_sexo;
	}
	public void setPes_sexo(String pes_sexo) {
		this.pes_sexo = pes_sexo;
	}
	public String getPes_email() {
		return pes_email;
	}
	public void setPes_email(String pes_email) {
		this.pes_email = pes_email;
	}
	public String getPes_celular() {
		return pes_celular;
	}
	public void setPes_celular(String pes_celular) {
		this.pes_celular = pes_celular;
	}
	public String getPes_senha() {
		return pes_senha;
	}
	public void setPes_senha(String pes_senha) {
		this.pes_senha = pes_senha;
	}
	public String getPes_data_cadastro() {
		return pes_data_cadastro;
	}
	public void setPes_data_cadastro(String pes_data_cadastro) {
		this.pes_data_cadastro = pes_data_cadastro;
	}

}
